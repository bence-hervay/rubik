use std::{env, fs, io};

use rubik::{
    Byte, CenterCoordExpr, CenterLocationExpr, CenterScheduleStep, Cube, FaceCommutator, FaceId,
    FaceletLocation, MoveAngle,
};

const TEMPLATE_N: usize = 101;
const TEMPLATE_ROW: usize = 17;
const TEMPLATE_COLUMN: usize = 31;

fn main() -> io::Result<()> {
    let output = generate();

    if let Some(path) = env::args().nth(1) {
        fs::write(path, output)
    } else {
        print!("{output}");
        Ok(())
    }
}

fn generate() -> String {
    let cube = Cube::<Byte>::new_solved_with_threads(TEMPLATE_N, 1);
    let mut candidates = Vec::new();

    for destination in FaceId::ALL {
        for helper in FaceId::ALL {
            if helper == destination || helper == opposite_face(destination) {
                continue;
            }

            for angle in MoveAngle::ALL {
                let commutator = FaceCommutator::new(destination, helper, angle);
                for update in cube.normalized_face_commutator_sparse_updates(
                    commutator,
                    TEMPLATE_ROW,
                    TEMPLATE_COLUMN,
                ) {
                    if update.to.face != destination || update.from.face == destination {
                        continue;
                    }

                    candidates.push(CenterScheduleStep {
                        destination,
                        source: update.from.face,
                        helper,
                        angle,
                        source_location: location_expr(update.from),
                        destination_location: location_expr(update.to),
                    });
                }
            }
        }
    }

    let mut ordered = Vec::new();
    for destination in FaceId::ALL {
        for source in FaceId::ALL {
            for helper in FaceId::ALL {
                for angle in MoveAngle::ALL {
                    for candidate in candidates.iter().copied() {
                        if candidate.destination == destination
                            && candidate.source == source
                            && candidate.helper == helper
                            && candidate.angle == angle
                            && !ordered.contains(&candidate)
                        {
                            ordered.push(candidate);
                        }
                    }
                }
            }
        }
    }

    render(&ordered)
}

fn render(steps: &[CenterScheduleStep]) -> String {
    let mut out = String::new();
    out.push_str(
        "// Generated by `cargo run --bin generate_center_schedule -- src/solver/generated_center_schedule.rs`.\n",
    );
    out.push_str("use super::{\n");
    out.push_str("    CenterCoordExpr::{Column, ReverseColumn, ReverseRow, Row},\n");
    out.push_str("    CenterLocationExpr as Location,\n");
    out.push_str("    CenterScheduleStep as Step,\n");
    out.push_str("};\n");
    out.push_str("use crate::{\n");
    out.push_str("    FaceId::{B, D, F, L, R, U},\n");
    out.push_str("    MoveAngle::{Double, Negative, Positive},\n");
    out.push_str("};\n\n");
    out.push_str("pub const GENERATED_CENTER_SCHEDULE: &[Step] = &[\n");

    for step in steps {
        out.push_str("    Step {\n");
        out.push_str(&format!(
            "        destination: {},\n",
            face_name(step.destination)
        ));
        out.push_str(&format!("        source: {},\n", face_name(step.source)));
        out.push_str(&format!("        helper: {},\n", face_name(step.helper)));
        out.push_str(&format!("        angle: {},\n", angle_name(step.angle)));
        out.push_str(&format!(
            "        source_location: {},\n",
            location_expr_text(step.source_location)
        ));
        out.push_str(&format!(
            "        destination_location: {},\n",
            location_expr_text(step.destination_location)
        ));
        out.push_str("    },\n");
    }

    out.push_str("];\n");
    out
}

fn location_expr(location: FaceletLocation) -> CenterLocationExpr {
    CenterLocationExpr {
        face: location.face,
        row: coord_expr(location.row),
        column: coord_expr(location.col),
    }
}

fn coord_expr(value: usize) -> CenterCoordExpr {
    match value {
        TEMPLATE_ROW => CenterCoordExpr::Row,
        TEMPLATE_COLUMN => CenterCoordExpr::Column,
        value if value == TEMPLATE_N - 1 - TEMPLATE_ROW => CenterCoordExpr::ReverseRow,
        value if value == TEMPLATE_N - 1 - TEMPLATE_COLUMN => CenterCoordExpr::ReverseColumn,
        _ => panic!("coordinate {value} is not represented by row/column template expressions"),
    }
}

fn opposite_face(face: FaceId) -> FaceId {
    match face {
        FaceId::U => FaceId::D,
        FaceId::D => FaceId::U,
        FaceId::R => FaceId::L,
        FaceId::L => FaceId::R,
        FaceId::F => FaceId::B,
        FaceId::B => FaceId::F,
    }
}

fn location_expr_text(location: CenterLocationExpr) -> String {
    format!(
        "Location {{ face: {}, row: {}, column: {} }}",
        face_name(location.face),
        coord_expr_name(location.row),
        coord_expr_name(location.column)
    )
}

fn face_name(face: FaceId) -> &'static str {
    match face {
        FaceId::U => "U",
        FaceId::D => "D",
        FaceId::R => "R",
        FaceId::L => "L",
        FaceId::F => "F",
        FaceId::B => "B",
    }
}

fn angle_name(angle: MoveAngle) -> &'static str {
    match angle {
        MoveAngle::Positive => "Positive",
        MoveAngle::Double => "Double",
        MoveAngle::Negative => "Negative",
    }
}

fn coord_expr_name(expr: CenterCoordExpr) -> &'static str {
    match expr {
        CenterCoordExpr::Row => "Row",
        CenterCoordExpr::Column => "Column",
        CenterCoordExpr::ReverseRow => "ReverseRow",
        CenterCoordExpr::ReverseColumn => "ReverseColumn",
    }
}
